// Hàm nội suy song tuyến tính (Bilinear Interpolation)
function lookupPhiY(lambda, f) {
  // Giới hạn trong bảng
  let lb = Math.max(10, Math.min(220, lambda));
  let f_val = Math.max(200, Math.min(640, f));

  // Tìm chỉ số hàng (lambda)
  let r1 = 0, r2 = 0;
  for (let i = 0; i < PHI_Y_DATA.lambda.length - 1; i++) {
    if (lb >= PHI_Y_DATA.lambda[i] && lb <= PHI_Y_DATA.lambda[i+1]) {
      r1 = i; r2 = i + 1; break;
    }
  }
  if (lb === PHI_Y_DATA.lambda[PHI_Y_DATA.lambda.length - 1]) {
    r1 = r2 = PHI_Y_DATA.lambda.length - 1;
  }

  // Tìm chỉ số cột (f)
  let c1 = 0, c2 = 0;
  for (let j = 0; j < PHI_Y_DATA.f.length - 1; j++) {
    if (f_val >= PHI_Y_DATA.f[j] && f_val <= PHI_Y_DATA.f[j+1]) {
      c1 = j; c2 = j + 1; break;
    }
  }
  if (f_val === PHI_Y_DATA.f[PHI_Y_DATA.f.length - 1]) {
    c1 = c2 = PHI_Y_DATA.f.length - 1;
  }

  // Lấy 4 giá trị góc
  let v11 = PHI_Y_DATA.values[r1][c1] / 1000;
  let v12 = PHI_Y_DATA.values[r1][c2] / 1000;
  let v21 = PHI_Y_DATA.values[r2][c1] / 1000;
  let v22 = PHI_Y_DATA.values[r2][c2] / 1000;

  // Tính tỷ lệ nội suy
  let dL = r1 === r2 ? 0 : (lb - PHI_Y_DATA.lambda[r1]) / (PHI_Y_DATA.lambda[r2] - PHI_Y_DATA.lambda[r1]);
  let dF = c1 === c2 ? 0 : (f_val - PHI_Y_DATA.f[c1]) / (PHI_Y_DATA.f[c2] - PHI_Y_DATA.f[c1]);

  // Nội suy tuyến tính theo f
  let val1 = v11 + dF * (v12 - v11);
  let val2 = v21 + dF * (v22 - v21);

  // Nội suy tuyến tính theo lambda
  let finalVal = val1 + dL * (val2 - val1);

  return finalVal;
}
