// Hàm nội suy song tuyến tính (Bilinear Interpolation)
function lookupPhiE(lambda_bar, me) {
  // Giới hạn trong bảng
  let lb = Math.max(0.5, Math.min(14.0, lambda_bar));
  let m = Math.max(0.1, Math.min(20.0, me));

  // Tìm chỉ số hàng (lambda)
  let r1 = 0, r2 = 0;
  for (let i = 0; i < PHI_E_DATA.lambda.length - 1; i++) {
    if (lb >= PHI_E_DATA.lambda[i] && lb <= PHI_E_DATA.lambda[i+1]) {
      r1 = i; r2 = i + 1; break;
    }
  }
  if (lb === PHI_E_DATA.lambda[PHI_E_DATA.lambda.length - 1]) {
    r1 = r2 = PHI_E_DATA.lambda.length - 1;
  }

  // Tìm chỉ số cột (m)
  let c1 = 0, c2 = 0;
  for (let j = 0; j < PHI_E_DATA.m.length - 1; j++) {
    if (m >= PHI_E_DATA.m[j] && m <= PHI_E_DATA.m[j+1]) {
      c1 = j; c2 = j + 1; break;
    }
  }
  if (m === PHI_E_DATA.m[PHI_E_DATA.m.length - 1]) {
    c1 = c2 = PHI_E_DATA.m.length - 1;
  }

  // Lấy 4 giá trị góc
  let v11 = PHI_E_DATA.values[r1][c1] / 1000;
  let v12 = PHI_E_DATA.values[r1][c2] / 1000;
  let v21 = PHI_E_DATA.values[r2][c1] / 1000;
  let v22 = PHI_E_DATA.values[r2][c2] / 1000;

  // Tính tỷ lệ nội suy
  let dL = r1 === r2 ? 0 : (lb - PHI_E_DATA.lambda[r1]) / (PHI_E_DATA.lambda[r2] - PHI_E_DATA.lambda[r1]);
  let dM = c1 === c2 ? 0 : (m - PHI_E_DATA.m[c1]) / (PHI_E_DATA.m[c2] - PHI_E_DATA.m[c1]);

  // Nội suy tuyến tính theo m
  let val1 = v11 + dM * (v12 - v11);
  let val2 = v21 + dM * (v22 - v21);

  // Nội suy tuyến tính theo lambda
  let finalVal = val1 + dL * (val2 - val1);

  return finalVal;
}
